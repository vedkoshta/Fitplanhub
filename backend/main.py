from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from database import engine, Base
from routes import auth_routes, trainer_routes, plan_routes, user_routes

Base.metadata.create_all(bind=engine)

app = FastAPI(
    title="FitPlanHub API",
    description="A platform where trainers create and sell fitness plans",
    version="1.0.0"
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(auth_routes.router)
app.include_router(trainer_routes.router)
app.include_router(plan_routes.router)
app.include_router(user_routes.router)

@app.get("/")
def root():
    return {"message": "Welcome to FitPlanHub API", "docs": "/docs"}

@app.get("/health")
def health_check():
    return {"status": "healthy"}
